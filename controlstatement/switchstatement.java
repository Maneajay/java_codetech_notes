package controlstatement;

public class switchstatement {
	public static void main(String[] args) {
		String fruit= "mango" ;
		switch(fruit) {
		case "mango":
			System.out.println("this is mango");
			break;
		case "apple":
			System.out.println("this is apple");
			break;
		case "banana":
			System.out.println("this is banana");
			break;
		default:		
		}
	}

}
